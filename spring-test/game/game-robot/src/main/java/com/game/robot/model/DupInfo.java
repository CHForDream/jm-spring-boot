package com.game.robot.model;

public class DupInfo {
	private int dupId;
	private int dupNum;
	private int star;

	public int getDupId() {
		return dupId;
	}

	public int getDupNum() {
		return dupNum;
	}

	public int getStar() {
		return star;
	}

	public void setDupId(int dupId) {
		this.dupId = dupId;
	}

	public void setDupNum(int dupNum) {
		this.dupNum = dupNum;
	}

	public void setStar(int star) {
		this.star = star;
	}
}
